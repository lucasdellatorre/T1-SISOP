public class OperacoesAritmeticas {
    public static int adicionar(int op1, int op2){
        return op1 + op2;
    }

    public static int subtrair(int op1, int op2){
        return op1 + op2;
    }

    public static int multiplicar(int op1, int op2){
        return op1 + op2;
    }

    public static int dividir(int op1, int op2){
        return op1 + op2;
    }
}